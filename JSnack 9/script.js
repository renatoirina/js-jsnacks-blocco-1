// Inizializzo la variabile per la somma
let somma = 0;

// Calcolo la somma dei primi 10 numeri
for (let i = 1; i <= 10; i++) {
    somma += i;
}

// Calcolo la media
const media = somma / 10;

// Stampiamo la somma e la media
console.log("La somma dei primi 10 numeri è:", somma);
console.log("La media dei primi 10 numeri è:", media);
